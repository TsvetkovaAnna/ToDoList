import Foundation

class MathFunc {
    func addNumb(x: Int, y: Int) -> Int {
        x + y
    }
    
    func multipleNumb(x: Int, y: Int) -> Int {
        x * y
    }
    
    func divideNumb(x: Int, y: Int) -> Int {
        x / y
    }
}
